                                    <h2> How To Reach Torna Fort </h2>
                                </div>
                                <div id="collapse4" class="collapse accordion-item">
                                    <div class="card-body article"> Torna Fort is located about 54 kilometres away from
                                        Pune in the Sahyadri Mountain Ranges. Travellers coming to Pune and disembarking
                                        at Pune Station Bus Stand, Pune Railway Junction or Pune Airport, would have to
                                        travel further till Velhe Village by road. Public transport is good enough in
                                        Pune and one can choose between a city bus, auto rickshaws, OLA, Uber or other
                                        tourist rental cabs to cover the distance till the base of the
                                        fort.<br /><br />It takes about 2.5 hours to reach Velhe from Pune Station Bus
                                        Stand or Pune Railway Station which are located approximately 60 kilometres
                                        away. The route to the base of the fort goes via Station Road &ndash; Sadhu
                                        Vaswani Road &ndash; Raja Bahadur Motilal Road &ndash; R T O Chowk &ndash;
                                        Abhiyantriki Maha Vidyalaya Chowk &ndash; NH 60/ NH 65/ SH 60 &ndash; J M Road

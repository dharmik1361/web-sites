                        <h2 class="display-4"> TORAN FORT </h2>
                        <p class="py-5">The toran Fort or Durg is found on the Old Mumbai and Pune route and it is
                            just next to the Prabalgad Fort. In this route, you have to trek up the rock-cut steps in
                            the mountain which zigzag their way up to the fort. The steps are exposed and you get to
                            view the whole surroundings when you are climbing them. The fort is situated at the top of
                            the mountain which is about 2,300 ft above the sea level. In the end, you reach the fort
                            which is covered by greens and it provides you with one of the best views of the
                            surroundings. It is best to avoid in monsoon season if you are willing to trek to this fort.
                            Trekking Duration: 1 Day
                            Difficulty level: Medium
                            Trekking Distance: 8 Km </p>

                            <div class="card">
                                <div class="card-header" id="heading1" data-toggle="collapse" data-target="#collapse1">
                                    <h2> History of Torna Fort, Pune </h2>
                                </div>
                                <div id="collapse1" class="collapse accordion-item">
                                    <div class="card-body article"> &nbsp;<br /><br />History scholars believe that
                                        Torna Fort was built by Shaivites in the 13th century. The temple at the
                                        entrance of the fort belongs to Menghai Devi and was called &ldquo;Tornaji
                                        Temple&rdquo;. This structure supports the fact that it was built in the 13th
                                        century. Four centuries later, in 1646, Shivaji Maharaj conquered the fort. He
                                        was a teenager at the time (barely 16 years old) and made a huge impact on the
                                        Maratha Empire by acquiring the fort. He then renamed the fort,
                                        &ldquo;Prachandagad&rdquo; (Prachanda meaning massive and Gad meaning
                                        fort).<br /><br />Torna Fort was controlled by the Marathas till the 18th
                                        century by the next generation of Chhatrapati Shivaji Maharaj. After the
                                        assassination of his first son, Sambhaji Maharaj, the Mughal Empire took over
                                        the fort. Aurangzeb even renamed it as &ldquo;Futulgaib&rdquo;. Some even say
                                        that this war to win the Torna Fort was the first ever real war Aurangzeb
                                        fought. Later, according to the Treaty of Purandar (signed in 1776), it was
                                        restored back to the Marathi Empire and continued staying the core of Maratha
                                        Empire for quite some time after.<br /> </div>
                                </div>
                            </div>

                            <div class="card">
                                <div class="card-header" id="heading2" data-toggle="collapse" data-target="#collapse2">
                                    <h2> The Architecture of Torna Fort, Pune </h2>
                                </div>
                                <div id="collapse2" class="collapse accordion-item">
                                    <div class="card-body article"> Torna Fort is built on a hill in the Western Ghats
                                        of India (Sahyadri Mountain Range) at a height of over 1400 metres above sea
                                        level. It consists of tall stone ramparts that enclose the fort ruins. There are
                                        seven gates on the pathway/ stairway that leads to the entrance. The first gate
                                        closer to the base is called the Bini Darwaza and the main or the last entrance
                                        is called the Kothi Darwaza. A narrow lane with tall walls on either side
                                        extends within the fort premises leading to some more parts of the citadel which
                                        also consists of plain stretch of lands in various shapes. Some of the ruins
                                        also signify granaries, guard rooms and resting areas. The fort also has several
                                        water tanks and some temples around.<br /> </div>
                                </div>
                            </div>

                            <div class="card">
                                <div class="card-header" id="heading3" data-toggle="collapse" data-target="#collapse3">
                                    <h2> Things to do at Torna Fort, Pune </h2>
                                </div>
                                <div id="collapse3" class="collapse accordion-item">
                                    <div class="card-body article"> Torna Fort is famous for adventure activities like
                                        trekking, hiking and camping. The beautiful surroundings of the fort in the
                                        Sahyadri&rsquo;s have ample spots for camping. The slopes of the mountains are
                                        perfect for advanced level of treks as the difficulty level is high. It consists
                                        of an initial climb through a muddy yet forested area at the base. During
                                        monsoons, this area becomes slippery and therefore, requires trekkers to be
                                        careful. Then there is a gradual ascent followed by a rocky patch beyond which
                                        is the plateau where Torna Fort is located.<br /><br />Some might also be
                                        interested in sightseeing as there are waterfalls, picturesque valleys and
                                        viewpoints, that are also great spots for picnics and outings. Many may not
                                        choose to trek through but instead, they can drive to the points and hang out
                                        for a while. Visitors may also want to give landscape photography a try even if
                                        they are amateurs. During monsoons, especially, the clouds hang down on the
                                        mountain peaks creating a stunning view of the scenic
                                        surrounding.<br /><br />The fort itself has several sections that tourists can
                                        explore. Upon entering the fort, people would walk into the Budhala Machche
                                        which is a wide expanse of grassland. As they walk ahead, they would come across
                                        temples, ruins, water tanks etc. Once they reach the topmost part of the fort,
                                        they can even get a stunning view of Sinhagad Fort, Khadakwasla Dam,
                                        Mahabaleshwar, Bhatghar Dam, Raigad and Pratapgad.<br /> </div>
                                </div>
                            </div>


                        <h2 class="display-4"> RAJGHAD FORT </h2>
                        <p class="py-5">rajghad Fort is another good trekking spot situated in the Igatpuri region. The
                            fort appears rectangular from the base and the resting rock of the fort is triangular in
                            shape. The walls of the trekking way are vertical and steps have been carved into the rock.
                            It is preferable to explore the trek during the monsoon season, but you should have a
                            professional guide with you. There is a temple at the top and you will get to have an
                            amazing view of the environment and surroundings when you are trekking to the top.
                            Trekking Duration: 1 Day
                            Difficulty level: Medium to Hard </p>

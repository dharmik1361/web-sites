                                    <br />
                                    <h3>6. Machis</h3>
                                    Rajgad Fort is also famous for the Sanjivani machi, Suvela machi and Padmavati
                                    machi. The Padmavati machi was used as the main structure to protect the fort,
                                    during the reign of Marathas.

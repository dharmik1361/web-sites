                        <div class="card">
                            <div class="card-header" id="heading2" data-toggle="collapse" data-target="#collapse2">
                                <h2> Places to See </h2>
                            </div>
                            <div id="collapse2" class="collapse accordion-item">
                                <div class="card-body article">
                                    <h3>1. Padmavat Temple</h3>
                                    A temple located inside the fortress is dedicated to the goddess Padmavati, this is
                                    one of the are major attractions. Goddess Padmavati is another form of Goddess Maha
                                    Lakshmi and is also called Alamelu. It is also used by the trekkers for overnight
                                    stay. This temple was constructed by Chatrapati Shivaji Maharaj himself.<br />
                                    <h3>2. Rameshwar Temple</h3>
                                    You can enjoy the beauty of the Rameshwar temple which is located within the fort.
                                    This temple is dedicated to the Hindu deity Lord Shiva, also called Lord Rameshwar.
                                    There is a unique&nbsp;replica&nbsp;of Shiva Linga that dates back to the times of
                                    Shivaji, around the 1600s. This is a smaller temple that lies in the front yard of
                                    Padmavat temple. <br />
                                    <h3>3. Saibai's Tomb</h3>
                                    Among Shivaji's eight wives, Sai Bhosale was his chief companion and his first wife
                                    too. It is believed that he loved Saibai a lot. But the beloved couple wasn't meant
                                    to be together for long. Saibai breathed her last at the Rajgad fort and a tomb was
                                    constructed in her memory.<br />
                                    <h3>4. Padmavati Lake</h3>
                                    It is a small freshwater lake. Its walls are intact even today and this lake never
                                    dries up. The Chor darwaja or the secret doorway leads to the Padmavati lake
                                    directly. You can pretty easily spot small fishes in the Padmavati lake.<br />
                                    <h3>5. Alu, Pali and Gunjavane Darwazas</h3>
                                    The fort is adorned by several doors including the Gunjavane darwaja, Alu darwaja
                                    and Pali darwaja. These darwazas are nothing but entrances to the Rajgad fort. And
                                    these elaborate doorways also served as guards to the fortress. The Alu darwaja was
                                    used in the olden times to connect the Rajgad fort to the Torna fort, but it now
                                    lies in ruins. There are several citadels as well. While the Pali darwaja, which is
                                    the main door of Rajgad fort, is still intact. There is also a secret back door that
                                    leads to the fort called the 'Chor Darwaja'. A steep, narrow and curved staircase
                                    that leads to it. This was built as a secret escape at times of crisis. Similar to
                                    the Chor darwaja, there's another secret doorway called the 'Madhe Darwaja' that
                                    extends from the Suvela Machi.<br /><br />
                                    <figure class="image" style="text-align: center;"><img

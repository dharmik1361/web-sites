                            <div class="card-header" id="heading1" data-toggle="collapse" data-target="#collapse1">
                                <h2> How To Reach Rajgad Fort </h2>
                            </div>
                            <div id="collapse1" class="collapse accordion-item">
                                <div class="card-body article"> Direct flights are easily available from major cities to
                                    Pune. And from Pune, state transport buses are available till Gunjawane. From
                                    Gunjawane share rickshaws or Local transport takes you to the village of Rajgad.
                                    Also there is a direct route via the Pune-Satara highway. </div>

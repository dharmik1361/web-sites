                        <div class="card">
                            <div class="card-header" id="heading3" data-toggle="collapse" data-target="#collapse3">
                                <h2> History </h2>
                            </div>
                            <div id="collapse3" class="collapse accordion-item">
                                <div class="card-body article"> Rajgad was known as the capital of the 'Hindavi
                                    Swarajya' (Hindu Kingdom) and is believed to be the king of forts in India. Its
                                    history&nbsp;dates back to 1646-1647. The fort of Rajgad stood witness to several
                                    important battles of the Marathas. It was captured by the Mughals in 1689 after
                                    Shivaji's son Sambhaji died. It is believed that Shivaji spent most of his life at
                                    the Rajgad fort. </div>
                            </div>
                        </div>

                        <div class="card">
                            <div class="card-header" id="heading4" data-toggle="collapse" data-target="#collapse4">
                                <h2> Camping at Rajgad Fort </h2>
                            </div>
                            <div id="collapse4" class="collapse accordion-item">
                                <div class="card-body article"> Since there is a lot to see in and around the fort, you
                                    possibly need more than a day to properly explore it. You can either put up inside
                                    the two temples or even camp in the fort. Stay overnight at this magnificent fort
                                    and enjoy camping in the fort by putting up your tents and lighting a bonfire, if
                                    you are visiting it in winters. There is plenty of room in the Padmavat Temple and
                                    it can easily accommodate 50-60 people. There is also a smaller temple within the
                                    premises of the fort that can accommodate around 8-10 people. If you have a tent
                                    then you can simply pitch your tent in the large area available. There is no entry
                                    fee and the fort remains open to visitors the entire week. It is preferable to carry
                                    some snacks and water is you are planning to put up at the fort for a night. </div>
                            </div>
                        </div>

